<p><b>IsItWeekA didn't look like this before!</b><br/>
The original IsItWeekA.com was run by Hasan Ali and the editorial team of the <a href='http://www.pupilsvoice.com'>Pupils' Voice</a>. However, in July 2011, Hasan and the rest of the original PV editors left the school, and the site ceased to be updated.</p>

<p><b>What's different about this incarnation?</b><br />
This new site is powered by a database of each day and what school week it is. We currently have data as far as July 2012, with provisional data up to the end of October. This means it does not need frequent updating - only a spreadsheet fed into it every few months.</p>

<p><b>Who runs this site now?</b><br />
This site is maintained by Joe Malt in Year 9, with help from Matt Else.</p>

